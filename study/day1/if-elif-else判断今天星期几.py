taday=int(input("请输入数字1--7："))
if taday==1:
    print ("今天星期一")
elif taday==2:
    print ("今天星期二")
elif taday==3:
    print ("今天星期三")
elif taday==4:
    print ("今天星期四")
elif taday==5:
    print ("今天星期五")
elif taday==6:
    print ("今天星期六")
elif taday==7:
    print ("今天星期天")
else:
    print ("输入的数值有误···")