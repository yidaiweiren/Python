#使用while循环打印倒三角形
#\t相当于一个制表位
#\n换行
i = 1
while i<=10:#大坑，这里是你要执行几行的参数，正常i+=1,就ok
    #print ("这是%d"%i)
    j = 10
    while j>=i:#i=1，j>=1,打印一个值，循环，j-=1,一共有十个数，十个结果，执行十次
        #然后下面依此类推
        print ("*\t",end="")
        j-=1
    print ("")
    i+=1

#结果
