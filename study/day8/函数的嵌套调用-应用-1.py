#函数的嵌套调用-应用
#打印五条双横线

#创建第一个模块（函数），打印一条双横线
def print_line():
    print ("="*50)

def print_5_line():
    i=0
    while i<5:  #使用while循环，循环打印出来5条横线
        print_line()
        i+=1

print_5_line()      #打印5条线


