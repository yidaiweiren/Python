#不定长参数
#**表示可以保存实参中带有“变量名”的参数
def test(a,b,c=33,*args,**kwargs):
    print (a)
    print (b)
    print (c)
    print (args)
    print (kwargs)

test(11,22,33,44,55,66,77)
'''
11
22
33
(44, 55, 66, 77)
{}  **kwargs打印出来是一个空的字典，为什么没有值，因为单个的数值都存储在*args中
'''

#现在给实参中添加一个计划（task）和完成（done）的变量

test(11,22,33,44,55,66,77,task=50,done=45)
'''
11
22
33
(44, 55, 66, 77)
{'task': 50, 'done': 45}    直接将"最后"的两个带变量的，以键值对的形式传递给**kwargs，在写入字典中
'''