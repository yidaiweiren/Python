#指定“实参”传递给谁

def test3(a,b=22,c=33):
    print (a)
    print (b)
    print (c)

test3(22,c=55)     #不给b传递参数，然后就可以跳过b，将参数传递给c，指定实参传递给谁。像这种直接给变量传值，变量名必须与“形参”中的变量名一致

