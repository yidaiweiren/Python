#上述方法可以实现功能，但是发现并不是最优解，而且发现传递过程中，有一个参数是重复出现的，可以进行优化，直接将重复参数写入“形参”，详情如下
def test2(a,b=22):  #形参只能放在最后面，不能放前面
    result=a+b
    print (result)
    print ("*"*10)

test2(11)
test2(22)
test2(33)
test2(44)
test2(55,33)    #结果为88，当有"实参"时，实参会默认代替形参

