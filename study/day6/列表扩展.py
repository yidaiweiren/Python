#循环一次删除例表中的元素的坑
#大多会出现在“越界”行为中

a = [11,22,33,44,55,66,77]

for i in a:
    if i==33 or i==44:
        a.remove(i)
print (a)
'''
这就是坑，程序删掉33后，44顶到33的位置，下标发生变化，程序在原下标找不到了，所以删除不掉
[11, 22, 44, 55, 66, 77]
'''

#解决方案
b = [11,22,33,44,55,66,77]

#找一个列表先把需要删除的存起来，然后再删
c = []

for j in b:
    if j==33 or j==44:
        c.append(j)

print (c)
print (b)

for x in c:
   b.remove(x)

print (b)

'''
[33, 44]
[11, 22, 33, 44, 55, 66, 77]
[11, 22, 55, 66, 77]
'''