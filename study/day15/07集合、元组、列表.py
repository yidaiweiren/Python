#集合、元组、列表
#集合中存储的数据是没有相同的
#集合和字典都用{}定义

a = (11,22,33,11,22,33)
print (a)
print (type(a))
'''
(11, 22, 33, 11, 22, 33)
<class 'tuple'>
'''

b = [11,22,33,11,22,33]
print (b)
print (type(b))
'''
[11, 22, 33, 11, 22, 33]
<class 'list'>
'''

c = {11,22,33,11,22,33}
print (c)
print (type(c))
'''
{33, 11, 22}
<class 'set'>   set:集合
'''

